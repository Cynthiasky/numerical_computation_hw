function [res] = tr_inter(f0,f1,f2,f3,x)
    l0 = ((x - f1(1)) * (x - f2(1)) * (x - f3(1)) / ...
        ((f0(1) - f1(1)) * (f0(1) - f2(1)) * (f0(1) - f3(1))));
    
    l1 = ((x - f0(1)) * (x - f2(1)) * (x - f3(1))) / ...
        ((f1(1) - f0(1)) * (f1(1) - f2(1)) * (f1(1) - f3(1)));
    
    l2 = ((x - f0(1)) * (x - f1(1)) * (x - f3(1))) / ...
        ((f2(1) - f0(1)) * (f2(1) - f1(1)) * (f2(1) - f3(1)));
    
    l3 = ((x - f0(1)) * (x - f1(1)) * (x - f2(1))) / ...
        ((f3(1) - f0(1)) * (f3(1) - f1(1)) * (f3(1) - f2(1)));
    
    res = f0(2) * l0 + f1(2) * l1 + f2(2) * l2 + f3(2) * l3;
end

